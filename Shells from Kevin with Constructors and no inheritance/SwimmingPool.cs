public class SwimmingPool
{
    public SwimmingPool(int locNo, int aID, int numLanes, int sizePool){
        Location_NO = locNo;
        Amenities_ID = aID;
        Num_Lanes = numLanes;
        PoolSize = sizePool; 
    }
    public SwimmingPool()
    {
        public Int Location_NO { get; set; }
        public Int Amenities_ID { get; set; }
        public Int Num_Lanes { get; set; }
        public Int PoolSize { get; set; }
    }
}