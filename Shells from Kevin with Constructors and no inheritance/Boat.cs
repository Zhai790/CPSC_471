public class Boat
{
    public Boat(int boatID, string boatName, string boatType, int sailorLicense){
        Boat_ID = boatID;
        BName = boatName;
        BType = boatType;
        Sailor_License_NO = sailorLicense; 
    }
    public Boat()
    {
        public Int Boat_ID { get; set; }
        public String BName { get; set; }
        public String BType { get; set; }
        public Int Sailor_License_NO { get; set; }
    }
}